function output = finding_Y(Y)

E = [-29.1512300456521;0.543115389069763;1.14048132564499;-4.51921348508839;6.79727761668184;0.202561407181207];
C = [-0.00345655674752156,0.0968099515979540,0.0841099123332684,0.0902904274621892,-0.00455843483511483,3.98355210048657;-0.0497432643014157,-0.0392554786086129,-0.0323367933292096,0.0640661589790229,-0.00377724055292664,-1.02931775016060];

E=E(2:5,:);
C=C(:,2:5);

dum3 = size(C);
ny = dum3(1,1);
CEp=(((C*E)'*(C*E))^-1)*(C*E)';
% P1 = eye(6,6)-E*CEp*C;

P1 = eye(4,4)-E*CEp*C;

P2 = (eye(ny,ny)-(C*E)*CEp)*C;

if ny == 2
%     Y1 = Y(1,1:6)';
%     Y2 = Y(1,7:end)';
    Y1 = Y(1,1:4)';
    Y2 = Y(1,5:end)';
    Y = [Y1 Y2];
else
    Y = Y';
end

output1 = (P1+Y*P2)*E;
output = sumsqr(output1);
