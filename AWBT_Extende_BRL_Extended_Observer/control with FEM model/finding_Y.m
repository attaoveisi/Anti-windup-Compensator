function output = finding_Y(Y)
Y = Y';
E = [0;-5.42650000000000;0;5.07950000000000];
% E = [0;-5.42650000000000;0;5.07950000000000;0;5.00350000000000];
C = [0,-5.10120000000000,0,4.02600000000000];
% C = [0,-5.10120000000000,0,4.02600000000000,0,3.27260000000000];
CEp=((C*E)'*(C*E))^-1;
P1 = eye(4,4)-E*CEp*C;
% P1 = eye(6,6)-E*CEp*C;
P2 = (eye(1,1)-(C*E)*CEp)*C;
output1 = (P1+Y*P2)*E;
output = sumsqr(output1);
