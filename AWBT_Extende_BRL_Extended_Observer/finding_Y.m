function output = finding_Y(Y)
Y = Y';
E = [0.258916103898922;0.101956592759767;-1.80129126804772;-4.56185147008189];
C = [-23.1554338545303,9.56281796834669,0.435674513053965,-0.466194198179862];
CEp=((C*E)'*(C*E))^-1;
P1 = eye(4,4)-E*CEp*C;
P2 = (eye(1,1)-(C*E)*CEp)*C;
output1 = (P1+Y*P2)*E;
output = sumsqr(output1);
