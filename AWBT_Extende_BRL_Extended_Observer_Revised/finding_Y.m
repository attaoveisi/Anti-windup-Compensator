function output = finding_Y(Y)

E = [2997.32485292742;5416.63486449908;-1433.33551307862;82.5617392050175;-426.512400067190;-192.679840378493];
C = [-41.6645962626208,25.6343165045033,-1.45941940942197,24.6236536653662,12.2741172027955,67.0293094169251;16.4509176513094,-8.89201409404608,4.50114993729110,8.41747838296307,1.54276704494250,-26.3339493864308];

E=E(1:4,:);
C=C(:,1:4);

dum3 = size(C);
ny = dum3(1,1);
CEp=(((C*E)'*(C*E))^-1)*(C*E)';
% P1 = eye(6,6)-E*CEp*C;

P1 = eye(4,4)-E*CEp*C;

P2 = (eye(ny,ny)-(C*E)*CEp)*C;

if ny == 2
%     Y1 = Y(1,1:6)';
%     Y2 = Y(1,7:end)';
    Y1 = Y(1,1:4)';
    Y2 = Y(1,5:end)';
    Y = [Y1 Y2];
else
    Y = Y';
end

output1 = (P1+Y*P2)*E;
output = sumsqr(output1);
